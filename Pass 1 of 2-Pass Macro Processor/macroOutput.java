package macroPass1;

public class macroOutput {
	private String IC;
	private String MNT;
	private String MDT;
	private String KPDTAB;
	private String PNTAB;
	
	public void setIC(String IC){
		this.IC = IC;
	}
	
	public String getIC(){
		return this.IC;
	}
	
	public void setMNT(String MNT){
		this.MNT = MNT;
	}
	
	public String getMNT(){
		return this.MNT;
	}
	
	public void setMDT(String MDT){
		this.MDT = MDT;
	}
	
	public String getMDT(){
		return this.MDT;
	}
	
	public void setKPDTAB(String KPDTAB){
		this.KPDTAB = KPDTAB;
	}
	
	public String getKPDTAB(){
		return this.KPDTAB;
	}
	
	public void setPNTAB(String PNTAB){
		this.PNTAB = PNTAB;
	}
	
	public String getPNTAB(){
		return this.PNTAB;
	}
}
